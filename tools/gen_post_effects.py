"""Dev tool: generates the 350 shield screen post-effect JSONs.

Emits src/main/resources/assets/bubbleshield/post_effect/effect_00.json ...
effect_349.json (the %02d name format never truncates 3-digit ids, so ids
0..104 keep their historical file names).
The assets live in the main (not client) source set so the
postEffectAssetsExist game test can find them on the dedicated-server classpath;
the client resource manager loads them from the merged mod jar all the same.
Not wired into the build; rerun manually after changing the mapping below and
commit the regenerated files:

    python3 tools/gen_post_effects.py

Every effect now has its OWN screen shader: pass 1 of effect_NN.json references
bubbleshield:screenfx/sfx_NNN (generated by tools/gen_screen_shaders.py) with
the standardized FxConfig uniform block. The EFFECTS table below mirrors
com.bubbleshield.effect.EffectRegistry row for row -- (argbPrimary,
argbSecondary, screen FAMILY name) -- and is asserted against a parse of
EffectRegistry.java at generation time so the two can never drift.

FxConfig packing (must match the GLSL block `layout(std140) uniform FxConfig
{ vec4 Primary; vec4 Secondary; vec4 ParamsA; vec4 ParamsB; }` in that EXACT
member order; tools/validate_shaders.py regex-checks JSON order against the
GLSL of every referenced sfx file):

* Primary / Secondary: the effect's palette as normalized RGBA.
* ParamsA = [Speed, Strength, Scale, Aux] -- family-interpreted knobs, all
  derived from the FROZEN per-id derivation (paramA = 0.3 + 0.012 * id,
  paramB = 0.4 + ((id * 37) % PARAM_CYCLE) / PARAM_CYCLE; PARAM_CYCLE = 75,
  mirrored from EffectRegistry -- keep in lockstep). Per family:
      Speed     = paramA for every family (animation rate);
      Strength  = the family's main knob, same formula the V1 template
                  uniforms used (e.g. tint 0.5*paramB, wobble amplitude
                  0.004*paramB, pixelate mosaic 2+2*paramB, posterize levels
                  5+3*paramB, ... see FAMILY_KNOBS);
      Scale     = the family's frequency/cell knob where one existed
                  (wobble 24, ripple 48, scanlines 160+180*min(paramA, 2.0),
                  frostlens 10+20*paramA, moire 90+120*paramA), else 0;
      Aux       = the family's secondary knob where one existed (bloomglow
                  threshold 0.35+0.25*min(paramA, 1.55), pixelate posterize
                  resolution 24), else 0. The min() clamps only bite above
                  id 104 (see FAMILY_KNOBS comments).
* ParamsB = [Phase, Drift, TintMix, LumaFloor]:
      Phase     = fract(id * golden ratio) -- de-syncs same-family bubbles;
      Drift     = 0.6 + 0.8 * fract(id * (2 - golden ratio)) -- secondary
                  motion rate for the drift/surge animation modes;
      TintMix   = the family's palette-lean weight (the constants the 16
                  hand-written templates hard-coded, e.g. wobble 0.15,
                  chroma 0.1, glitch 0.25);
      LumaFloor = 0.35 -- the gameplay-safety luminance floor every generated
                  sfx shader applies (never crush the world below 0.35x).

Pass 2 is the unchanged vanilla blit (a pass cannot read and write the same
target). Each JSON follows the vanilla post_effect schema (see
assets/minecraft/post_effect/creeper.json in 26.2).
"""

